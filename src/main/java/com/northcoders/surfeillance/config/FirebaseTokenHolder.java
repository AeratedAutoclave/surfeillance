package com.northcoders.surfeillance.config;

public class FirebaseTokenHolder {
    private String uid;
    private String email;

    // Getters and Setters

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
